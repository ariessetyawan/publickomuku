class KomuKuYJB_Route_PrefixAdmin_Packages implements XenForo_Route_Interface, XenForo_Route_BuilderInterface
{
    public function match($routePath, Zend_Controller_Request_Http $request, XenForo_Router $router)
    {
        $action = $router->resolveActionWithIntegerParam($routePath, $request, 'package_id');
        return $router->getRouteMatch('KomuKuYJB_ControllerAdmin_Package', $action, 'classifiedPackages');
    }

    public function buildLink($originalPrefix, $outputPrefix, $action, $extension, $data, array &$extraParams)
    {
        return XenForo_Link::buildBasicLinkWithIntegerParam($outputPrefix, $action, $extension, $data, 'package_id', 'title');
    }
} 