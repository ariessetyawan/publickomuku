class GFNClassifieds_Route_PrefixAdmin_Fields implements XenForo_Route_Interface, XenForo_Route_BuilderInterface
{
    public function match($routePath, Zend_Controller_Request_Http $request, XenForo_Router $router)
    {
        $action = $router->resolveActionWithStringParam($routePath, $request, 'field_id');
        return $router->getRouteMatch('GFNClassifieds_ControllerAdmin_Field', $action, 'classifiedFields');
    }

    public function buildLink($originalPrefix, $outputPrefix, $action, $extension, $data, array &$extraParams)
    {
        return XenForo_Link::buildBasicLinkWithStringParam($outputPrefix, $action, $extension, $data, 'field_id');
    }
} 