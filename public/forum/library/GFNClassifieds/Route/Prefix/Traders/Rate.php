class GFNClassifieds_Route_Prefix_Traders_Rate implements XenForo_Route_Interface, XenForo_Route_BuilderInterface
{
    public function match($routePath, Zend_Controller_Request_Http $request, XenForo_Router $router)
    {
        $action = $router->resolveActionWithIntegerParam($routePath, $request, 'classified_id');
        return $router->getRouteMatch('GFNClassifieds_ControllerPublic_Trader', 'rate/' . $action);
    }

    public function buildLink($originalPrefix, $outputPrefix, $action, $extension, $data, array &$extraParams)
    {
        return XenForo_Link::buildBasicLinkWithIntegerParam($outputPrefix, $action, $extraParams, $data, 'classified_id');
    }
}