function kepembayaran(url)
{
  var form = $('<form></form>');
  $(form).hide().attr('method','post').attr('action',url);
  var token = $('<input type="hidden" />').attr('name','_token').val(CSRF_TOKEN);
  var user_id = $('<input type="hidden" />').attr('name','_user_id').val($('#user_id').html());
  var cekusername = $('<input type="hidden" />').attr('name','_cekusername').val($('#username').val());
  var pilihpaket = $('<input type="hidden" />').attr('name','_pilihpaket').val($('#kodepilihpaket').html());
  $(form).append(token);
  $(form).append(cekusername);
  $(form).append(user_id);
  $(form).append(pilihpaket);
  $(form).appendTo('body').submit();
}

$("#kirimuser").click(function() {
if($('#kirimuser').html() === "Kirim Permintaan"){
Lobibox.notify('info', { 
	size: 'mini', 
	msg: "Akan diteruskan ke halaman konfirmasi pembayaran"
});
$('#kirimuser').html("Tunggu Sebentar");
kepembayaran(APP_URL+'/upgrademember/pembayaran');	
}else{
$.ajax({
	url: APP_URL+'/usershaaphiere/1',
	type: 'post',
	data: {_token: CSRF_TOKEN,_cekusername: $('#username').val()},
	dataType: 'json',
	async: false,
	success: function (data) {
		if($('#kodepilihpaket').html() == ""){
			Lobibox.notify('info', { 
				size: 'normal', 
				msg: "Pilih Dulu Teman Paketnya"
			});
		}else if(data.success == false){
			Lobibox.notify('info', { 
				size: 'normal', 
				msg: "Username Tidak Ditemukan"
			});
		}else{
			Lobibox.notify('info', { 
				size: 'mini', 
				msg: "Username ditemukan, sekarang Kirim Permintaan ke staff"
			});
			$('#user_id').html(data.user_id);
			$('#kirimuser').html("Kirim Permintaan");
		}
	},
	error: function(jqXHR, textStatus, errorThrown) {
        if(textStatus==="timeout") {  
            Lobibox.notify('info', { 
				size: 'mini', 
				msg: "Server terlalu lama merespon (timeout)"
			});
			xhr.abort();
        } else {
            Lobibox.notify('info', { 
				size: 'mini', 
				msg: "Terjadi kesalahan yang lain, mohon tunggu sebentar"
			});
        }
    }
});
}
});	
